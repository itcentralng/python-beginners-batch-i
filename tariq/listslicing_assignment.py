# Q1. Create a list of 5 names
names = ["Alice", "Bob", "Charlie", "David", "Eva"]

# Q2. Get the second name using slicing
second_name = names[1]
print(second_name)

# Q3. Convert the second name to uppercase
second_name_uppercase = second_name.upper()

print("List of names:", names)
print("Second name:", second_name)
print("Second name in uppercase:", second_name_uppercase)