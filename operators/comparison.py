# Comparison operators are used to compare data

age1 = 10
age2 = 15

# == : Equals to => the two operand are the same
print(age1 == age2)
# != : Not equals to => the two operand are not the same
print(age1 != age2)
# > : Greater than => the left side of the operator is greater than the right side
print(age1 > age2)
# > : Greater than or equals => the left side of the operator is greater than or equals the right side
print(age1 >= age2)
# < : Less than => the left side of the operator is less than the right side
print(age1 < age2)
# < : Less than or equals => the left side of the operator is less than or equals the right side
print(age1 <= age2)